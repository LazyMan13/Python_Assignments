# escape code for tab
tabby_cat = "\tI'm tabbed in."
# escape code for new line
persian_cat = "I'm split\non a line."
# escape code for backslash
backslash_cat = "I'm \\ a \\ cat."
# triple-quote string list with codes for tabs and a newline in it
fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
'''
#printing all the variables
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
